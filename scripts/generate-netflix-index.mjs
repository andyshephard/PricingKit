import { writeFile } from 'node:fs/promises';

const SOURCE_REPO = 'https://github.com/tompec/netflix-prices';
const SOURCE_URL = 'https://raw.githubusercontent.com/tompec/netflix-prices/main/data/latest.json';
const SOURCE_LICENSE = 'CC-BY-4.0';
const SOURCE_COMMIT = process.env.NETFLIX_PRICES_COMMIT || 'a46477c39bc95049bca452b4476d0ed1ac64d1ea';
const OUTPUT_FILE = new URL('../src/lib/conversion-indexes/netflix.ts', import.meta.url);
const INFERRED_REGIONS = {
  CN: 'HK',
  XK: 'AL',
  RU: 'BY',
};

function clamp(value) {
  return Math.max(0.1, Math.min(2.0, value));
}

function round(value) {
  return Math.round(value * 10000) / 10000;
}

function getStandardPriceUsd(entry) {
  const standard = entry.plans?.find((plan) => plan.name === 'standard');
  return standard?.price_usd;
}

function renderEntry(code, entry) {
  const multiplier = Number.isInteger(entry.multiplier)
    ? entry.multiplier.toFixed(1)
    : String(entry.multiplier);
  const lines = [
    `  ${code}: {`,
    `    multiplier: ${multiplier},`,
    `    standardPriceUsd: ${entry.standardPriceUsd},`,
    `    source: '${entry.source}',`,
  ];
  if (entry.inferredFrom) {
    lines.push(`    inferredFrom: '${entry.inferredFrom}',`);
  }
  lines.push('  },');
  return lines.join('\n');
}

const response = await fetch(SOURCE_URL);
if (!response.ok) {
  throw new Error(`Failed to fetch ${SOURCE_URL}: ${response.status} ${response.statusText}`);
}

const data = await response.json();
const usEntry = data.find((entry) => entry.country_code === 'US');
const usStandardUsd = getStandardPriceUsd(usEntry);
if (!Number.isFinite(usStandardUsd)) {
  throw new Error('Missing US standard Netflix USD price');
}

const index = {};
for (const entry of data) {
  const code = entry.country_code;
  const standardPriceUsd = getStandardPriceUsd(entry);
  if (!/^[A-Z]{2}$/.test(code) || !Number.isFinite(standardPriceUsd)) {
    continue;
  }

  index[code] = {
    multiplier: round(clamp(standardPriceUsd / usStandardUsd)),
    standardPriceUsd,
    source: 'tompec-netflix-prices',
  };
}

for (const [code, inferredFrom] of Object.entries(INFERRED_REGIONS)) {
  const source = index[inferredFrom];
  if (!source) {
    throw new Error(`Cannot infer ${code}: source ${inferredFrom} is missing`);
  }
  index[code] = {
    multiplier: source.multiplier,
    standardPriceUsd: source.standardPriceUsd,
    source: 'inferred',
    inferredFrom,
  };
}

const sortedEntries = Object.entries(index).sort(([a], [b]) => a.localeCompare(b));
const contents = `// Netflix Price Index data for regional pricing\n// Source: tompec/netflix-prices, ${SOURCE_LICENSE}, ${SOURCE_REPO}\n// Source snapshot: ${SOURCE_URL}\n// Source commit: ${SOURCE_COMMIT}\n// Generated by: scripts/generate-netflix-index.mjs\n\nexport interface NetflixPriceIndexEntry {\n  multiplier: number;\n  standardPriceUsd: number;\n  source: 'tompec-netflix-prices' | 'inferred';\n  inferredFrom?: string;\n}\n\n// Netflix Standard ad-free subscription multipliers relative to US (US = 1.0).\n// Multiplier = country standard price in USD / US standard price in USD.\nexport const NETFLIX_PRICE_INDEX: Record<string, NetflixPriceIndexEntry> = {\n${sortedEntries.map(([code, entry]) => renderEntry(code, entry)).join('\n')}\n};\n\nexport const DEFAULT_NETFLIX_MULTIPLIER = 0.70;\n\nexport function getNetflixMultiplier(regionCode: string): number {\n  return NETFLIX_PRICE_INDEX[regionCode]?.multiplier ?? DEFAULT_NETFLIX_MULTIPLIER;\n}\n\nexport function getAllNetflixData(): Record<\n  string,\n  {\n    multiplier: number;\n    source: 'tompec-netflix-prices' | 'inferred' | 'default';\n    inferredFrom?: string;\n  }\n> {\n  const result: Record<\n    string,\n    {\n      multiplier: number;\n      source: 'tompec-netflix-prices' | 'inferred' | 'default';\n      inferredFrom?: string;\n    }\n  > = {};\n\n  for (const [regionCode, entry] of Object.entries(NETFLIX_PRICE_INDEX)) {\n    result[regionCode] = {\n      multiplier: entry.multiplier,\n      source: entry.source,\n      ...(entry.inferredFrom ? { inferredFrom: entry.inferredFrom } : {}),\n    };\n  }\n\n  return result;\n}\n`;

await writeFile(OUTPUT_FILE, contents);
console.log(`Generated ${OUTPUT_FILE.pathname} with ${sortedEntries.length} entries`);
